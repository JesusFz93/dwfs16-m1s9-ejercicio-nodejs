const resta = (num1, num2) => {
    return num1 - num2
}

const division = (num1, num2) => {
    return num1 / num2
}

const suma = (num1, num2) => {
    return num1 + num2
}

const multiplicacion = (num1, num2) => {
    return num1 * num2
}

module.exports = {
    resta, division, suma, multiplicacion
}